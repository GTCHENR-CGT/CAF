package com.gtchenr.pojo;

public class ReportStatue {

    private Integer reportStatueId;
    private String reportStatueName;
    private String reason;
    private Integer reportCredit;

    public Integer getReportStatueId() {
        return reportStatueId;
    }

    public void setReportStatueId(Integer reportStatueId) {
        this.reportStatueId = reportStatueId;
    }

    public String getReportStatueName() {
        return reportStatueName;
    }

    public void setReportStatueName(String reportStatueName) {
        this.reportStatueName = reportStatueName;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public Integer getReportCredit() {
        return reportCredit;
    }

    public void setReportCredit(Integer reportCredit) {
        this.reportCredit = reportCredit;
    }
}
